function changeStyle(n) {
    if (n === 1) {
        document.getElementById('styleSheet').href = 'style1.css';
    } else if (n === 2) {
        document.getElementById('styleSheet').href = 'style2.css';
    } else if (n === 3) {
        document.getElementById('styleSheet').href = 'style3.css';
    }
}
